    1. What is your greatest strength? - Best Answers
    This is one of the questions that employers almost always ask. When you are asked about your greatest strengths, it's important to discuss the attributes that will qualify you for the specific job and set you apart from the other candidates. Take the time, before the job interview, to make matches between your qualifications and the requirements as stated on the job announcement. This way, you will have examples ready to hand that will demonstrate your suitability for the job.

    2. What is your greatest weakness? - Best Answers
    Another typical question interviewers will ask is about your weaknesses.

    Do your best to frame your answers around positive aspects of your skills and abilities as an employee, turning seeming “weaknesses” into strengths. For example, you might say something like, “I’ve always struggled with perfectionism – I truly want to do the job correctly the first time, but this sometimes mean that I devote more time to a project than is necessary.

