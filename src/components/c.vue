    3. Tell me about yourself. - Best Answers
    Here’s how to answer questions about yourself without giving out too much – or too little – personal information. Start by sharing some of your personal interests and experiences which don't relate directly to work, such as a favorite hobby or a brief account of where you grew up, your education, and what motivates you. Remember to be politic, however – avoid potentially contentious subjects such as political or religious leanings, unless you are absolutely positive that your opinions would be well-received by your interviewer. You should also avoid talking too much about family responsibilities or hobbies that might make your interviewer wonder whether you could commit yourself 100% to the job.

    4. Why should we hire you? - Best Answers
